a = 10
b = 5

def add():
    return (a+b)

def sub():
    return (a-b)

def mul():
    return (a*b)

def div_int():
    return (a//b)

def modulo():
    return (a%b)

def div():
    return (a/b)

print("additon: ",add())
print("Substraction: ",sub())
print("division: ", div())
print("division int : ", div_int())
print("Multiplication: ",mul())
print("modulo(remainder): ",modulo())
